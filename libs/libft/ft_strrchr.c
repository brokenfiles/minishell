#include "../../includes/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*d;

	d = (char*)s;
	i = ft_strlen(d);
	while (i >= 0)
	{
		if (d[i] == (char)c)
			return (&d[i]);
		i--;
	}
	return (NULL);
}
