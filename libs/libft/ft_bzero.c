#include "../../includes/libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*d;
	unsigned int	i;

	d = (unsigned char*)s;
	i = 0;
	while (i < n)
	{
		d[i] = 0;
		i++;
	}
	s = (void*)d;
}
