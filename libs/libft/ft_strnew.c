#include "../../includes/libft.h"

char	*ft_strnew(int size)
{
	char *new;

	if (!(new = malloc(sizeof(char) * (size + 1))))
		return (NULL);
	new[size] = '\0';
	while (size--)
		new[size] = '\0';
	return (new);
}
