#include "../../includes/libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*now;

	now = lst;
	i = 0;
	while (now != NULL)
	{
		now = now->next;
		i++;
	}
	return (i);
}
