#include "../../includes/libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list *after;

	if (lst)
	{
		while (lst)
		{
			if (lst->next)
				after = lst->next;
			else
				after = NULL;
			(*f)(lst->content);
			lst = after;
		}
	}
}
